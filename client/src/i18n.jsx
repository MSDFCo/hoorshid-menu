const LanguageContext = createContext(null);

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('hoorshid-lang') || 'fa');

  useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
    localStorage.setItem('hoorshid-lang', lang);
  }, [lang]);

  const value = {
    lang,
    dir: lang === 'fa' ? 'rtl' : 'ltr',
    t: (key) => dict[lang][key] ?? key,
    toggle: () => setLang((l) => (l === 'fa' ? 'en' : 'fa')),
    // pick the right field from a bilingual DB record, e.g. L(item, 'name')
    L: (obj, field) => obj[`${field}_${lang}`] || obj[`${field}_${lang === 'fa' ? 'en' : 'fa'}`] || '',
  };

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLang() {
  return useContext(LanguageContext);
}
